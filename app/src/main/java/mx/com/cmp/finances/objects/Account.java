public class Account {

    String accountUI;
    String accountName;
    String accountType;
    String accountBalance;
    String accountIconColor;

    public void Accout() {
    }

    public String getAccountUI() {
        return accountUI;
    }

    public void setAccountUI(String accountUI) {
        this.accountUI = accountUI;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public String getAccountBalance() {
        return accountBalance;
    }

    public void setAccountBalance(String accountBalance) {
        this.accountBalance = accountBalance;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getAccountIconColor() {
        return accountIconColor;
    }

    public void setAccountIconColor(String accountIconColor) {
        this.accountIconColor = accountIconColor;
    }
}
